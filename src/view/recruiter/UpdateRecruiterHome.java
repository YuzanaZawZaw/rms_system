	private void updateButtonMouseClicked(java.awt.event.MouseEvent evt) {
		String first_name = updateFirstName.getText();
		String last_name = updateLastName.getText();
		String user_name = updateUsername.getText();
		String company_name = updateCompanyName.getText();
		String pass_word = updatePassword.getText();
		String phone_no = updatePhno.getText();
		String gen_der = checkMale.isSelected() ? "Male" : "Female";
		String e_mail = updateEmail.getText();
		String n_rc = updateNRC.getText();
		java.sql.Date birth_date = new java.sql.Date(dobCombo2.getDate()
				.getTime());
		// System.out.println("Birth date:::"+birth_date);
		String address = updateAddress.getText();
		String security_qest = updateSecurity.getText();
		int recruiter_id = Integer.parseInt(updateIdTxt.getText());

		UpdateRecruiter ds = new UpdateRecruiter(first_name, last_name,
				user_name, company_name, pass_word, phone_no, e_mail,
				birth_date, n_rc, gen_der, address, security_qest);
		ds.updateRecruiterProfile(recruiter_id);
		this.setVisible(false);
		String name = first_name + " " + last_name;
		RecruiterHome rh = new RecruiterHome(user_name, name);
		rh.setVisible(true);
	}

